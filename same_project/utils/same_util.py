import numpy as np

def add(x: np.ndarray, y: np.ndarray) -> np.ndarray:
    """
    return x + y.

    Parameters
    ----------
    x : np.ndarray
        Argument 1
    y : np.ndarray
        Argument 2

    Returns
    -------
    z : np.ndarray
        x + y
    """

    z: np.ndarray = x + y

    return z
